const (
	_UUIDAnnotationKey = "auth.actor_uuid"
)

// annotatedUUIDField describes a single field tagged with the auth.actor_uuid
// annotation, normalised for code generation.
//
// TypeName is the Go-side name of the type the accessor should hang off:
//   - for a top-level Thrift struct/exception, the struct's own Go name; and
//   - for a service method parameter, the synthetic args struct thriftrw
//     emits, named "<Service>_<Method>_Args".
//
// FieldName is the corresponding Go field name. thriftrw also emits a
// Get<FieldName>() string method for every field; the generated ActorUUID
// accessor delegates to it, so we don't need to track required-vs-optional
// ourselves (the getter already handles both cases and the nil receiver).
type annotatedUUIDField struct {
	TypeName  string
	FieldName string
}

const yarpcUUIDTemplate = `
// Code generated by thriftrw-plugin-yarpc
// @generated

package <.Name>

<range $f := getAllAnnotatedTypes>
// ActorUUID returns the UUID string from the field annotated with
// auth.actor_uuid, or "" if it is unset. Delegates to the thriftrw-generated
// Get<$f.FieldName> accessor so optional/required and nil-receiver handling
// stay in one place.
func (t *<$f.TypeName>) ActorUUID() string {
	return t.Get<$f.FieldName>()
}
<end>
`

func yarpcUUIDGenerator(data *moduleTemplateData, files map[string][]byte) error {
	// kv.thrift => .../kv/types_yarpc_uuid.go
	path := filepath.Join(data.Module.Directory, "types_yarpc_uuid.go")

	// data.CompiledModule is the result of compiling the original Thrift file.
	// Plugins do not have access to it via the API, so it is compiled once in
	// Generate and shared across generators; this generator writes extra
	// methods to types annotated with auth.actor_uuid in that Thrift file.
	compiledModule := data.CompiledModule

	annotatedTypes, err := anyAnnotatedTypes(compiledModule)
	if err != nil {
		return err
	}
	if len(annotatedTypes) == 0 {
		return nil
	}

	templateOptions := append(templateOptions,
		plugin.TemplateFunc("getAllAnnotatedTypes", func() []annotatedUUIDField {
			return annotatedTypes
		}),
	)

	files[path], err = plugin.GoFileFromTemplate(path, yarpcUUIDTemplate, compiledModule, templateOptions...)
	return err
}

// anyAnnotatedTypes collects every field carrying the auth.actor_uuid
// annotation across two locations:
//  1. top-level structs/exceptions declared in the Thrift module, and
//  2. method parameters on services defined in the Thrift module (which
//     thriftrw renders as synthetic "<Service>_<Method>_Args" structs).
//
// The annotation must appear at most once per containing struct/method.
//
// The returned slice is sorted by (TypeName, FieldName). compile.Module
// surfaces both Types and Services as Go maps, and we also walk each
// service's Functions map; iteration order across all three is
// non-deterministic, so an unsorted result would shuffle the order of
// accessors in the generated types_yarpc_uuid.go between runs and produce
// spurious diffs in downstream repos that check generated code in.
func anyAnnotatedTypes(module *compile.Module) ([]annotatedUUIDField, error) {
	var out []annotatedUUIDField

	for _, t := range module.Types {
		ss, ok := t.(*compile.StructSpec)
		if !ok {
			continue
		}
		fields, err := annotatedFieldsIn(ss.Fields, ss.Name)
		if err != nil {
			return nil, err
		}
		for _, f := range fields {
			out = append(out, annotatedUUIDField{
				TypeName:  goCase(ss.Name),
				FieldName: goCase(f.Name),
			})
		}
	}

	for _, svc := range module.Services {
		for _, fn := range svc.Functions {
			ownerLabel := fmt.Sprintf("%s.%s", svc.Name, fn.Name)
			fields, err := annotatedFieldsIn(fn.ArgsSpec, ownerLabel)
			if err != nil {
				return nil, err
			}
			for _, f := range fields {
				out = append(out, annotatedUUIDField{
					TypeName:  fmt.Sprintf("%s_%s_Args", goCase(svc.Name), goCase(fn.Name)),
					FieldName: goCase(f.Name),
				})
			}
		}
	}

	sort.Slice(out, func(i, j int) bool {
		if out[i].TypeName != out[j].TypeName {
			return out[i].TypeName < out[j].TypeName
		}
		return out[i].FieldName < out[j].FieldName
	})
	return out, nil
}

// annotatedFieldsIn returns the auth.actor_uuid-tagged fields within a single
// field group (a struct's fields or a method's args).
func annotatedFieldsIn(fields []*compile.FieldSpec, ownerLabel string) ([]*compile.FieldSpec, error) {
	var found []*compile.FieldSpec
	for _, field := range fields {
		if _, ok := field.Annotations[_UUIDAnnotationKey]; !ok {
			continue
		}
		if len(found) > 0 {
			return nil, fmt.Errorf(
				"multiple fields annotated with %s for %s. Prev %s; duplicate at %s",
				_UUIDAnnotationKey, ownerLabel, found[0].Name, field.Name)
		}
		found = append(found, field)
	}
	return found, nil
}

// The block below (goCase, pascalCase, isAllCaps, commonInitialisms) is
// copy-pasted verbatim from go.uber.org/thriftrw's internal gen package:
//
//	https://github.com/thriftrw/thriftrw-go/blob/master/gen/string.go
//
// thriftrw uses these helpers to derive Go identifiers from Thrift names
// during code generation. They are unexported there, so we cannot import
// them. We need the exact same naming logic to refer to thriftrw-generated
// types and fields (e.g. an "actor_uuid" field becomes "ActorUUID", not
// "Actor_uuid"); diverging here would produce code that does not compile.
//
// TODO: upstream a change to thriftrw that exports goCase (or an equivalent)
// and switch this plugin to use the exported version, then delete this copy.
//
// Note: strings.Title is deprecated as of Go 1.18, but is intentionally kept
// here to match thriftrw's behaviour exactly.

// isAllCaps checks if a string contains all capital letters only. Non-letters
// are not considered.
func isAllCaps(s string) bool {
	for _, r := range s {
		if unicode.IsLetter(r) && !unicode.IsUpper(r) {
			return false
		}
	}
	return true
}

// pascalCase combines the given words using PascalCase.
//
// If allowAllCaps is true, when an all-caps word that is not a known
// abbreviation is encountered, it is left unchanged. Otherwise, it is
// Titlecased.
func pascalCase(allowAllCaps bool, words ...string) string {
	for i, chunk := range words {
		if len(chunk) == 0 {
			// foo__bar
			continue
		}

		// known initalism
		init := strings.ToUpper(chunk)
		if _, ok := commonInitialisms[init]; ok {
			words[i] = init
			continue
		}

		// Was SCREAMING_SNAKE_CASE and not a known initialism so Titlecase it.
		if isAllCaps(chunk) && !allowAllCaps {
			// A single ALLCAPS word does not count as SCREAMING_SNAKE_CASE.
			// There must be at least one underscore.
			words[i] = strings.Title(strings.ToLower(chunk))
			continue
		}

		// Just another word, but could already be camelCased somehow, so just
		// change the first letter.
		head, headIndex := utf8.DecodeRuneInString(chunk)
		words[i] = string(unicode.ToUpper(head)) + string(chunk[headIndex:])
	}

	return strings.Join(words, "")
}

// goCase converts strings into PascalCase.
func goCase(s string) string {
	if len(s) == 0 {
		panic(fmt.Sprintf("%q is not a valid identifier", s))
	}

	words := strings.Split(s, "_")
	return pascalCase(len(words) == 1 /* all caps */, words...)
	// goCase allows all caps only if the string is a single all caps word.
	// That is, "FOO" is allowed but "FOO_BAR" is changed to "FooBar".
}

